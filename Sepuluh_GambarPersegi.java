public class Sepuluh_GambarPersegi {
    public static void main(String[] args) {
    	System.out.println("Menggambar Persegi Menggunakan #");
    	System.out.println("------------------------------");   	

        int end=5;

        for (int a=1;a<=end;a++){
            for (int b=1;b<=end;b++){
                System.out.print("#");
            }
            System.out.print("\n");
        }
    }
}
